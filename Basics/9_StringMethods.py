# help(str) for all string methods

print("################### String Methods ####################")
name = "seddawy 0 "
print(len(name))          # Counts all characters
print(name.find("s"))     # Finds the place of an alpha
print(name.capitalize())  # Makes the first alpha capital
print(name.upper())       # Makes all alpha capital
print(name.isdigit())     # Checks if all digits or not
print(name.strip())       # Deletes the spaces in the begining and the ending
print("#######################################################")


